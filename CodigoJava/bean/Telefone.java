package model.bean;

public class Telefone {
	
	private String Tipo_Tel;
	private String Numero;
	
	public String getTipo_Tel() {
		return Tipo_Tel;
	}
	public void setTipo_Tel(String tipo_Tel) {
		Tipo_Tel = tipo_Tel;
	}
	public String getNumero() {
		return Numero;
	}
	public void setNumero(String numero) {
		Numero = numero;
	}
	
	
}
